// 3 ways to assign variables

// 1. let - can be changed later
let fname = "John";

console.log(fname);
fname = "Johnny";
console.log(fname);

// 2. const - cannot be changed

const lname = "Bravo";

console.log(lname);

// 3. var - lagacy mo longer used or recommended

var mName = "James";

console.log(mName);
